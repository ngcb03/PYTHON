# Realizar un programa que imprima en pantalla los números del 0 al 100. Este problema lo 
# podemos resolver perfectamente con el ciclo while pero en esta situación lo resolveremos 
# empleando el for.

# Solución -> bucle for
for x in range(101):
  print(x)
  
# Solución -> bucle while
x=0
while x<101:
  print(x)
  x+=1  