# Calcular el sueldo mensual de un operario conociendo la cantidad de horas trabajadas y el valor por hora.

horasTrabajadas = int(input("Ingresa las horas trabajadas:"))
valorHora = int(input("Ingresa el valor por hora:"))
sueldo = horasTrabajadas*valorHora
print("El sueldo mensual del operario es",sueldo)
